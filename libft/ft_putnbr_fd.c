#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long		num;
	unsigned char	c;

	num = (long long)n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num *= -1;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	c = (num % 10) + '0';
	write(fd, &c, 1);
}
