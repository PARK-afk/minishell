#include "../../mini.h"

int	ft_pwd(void)
{
	char	*fwd;

	fwd = getcwd(NULL, 0);
	if (fwd)
	{
		printf("%s\n", fwd);
		free(fwd);
	}
	else
	{
		perror("pwd");
		return (1);
	}
	return (0);
}
